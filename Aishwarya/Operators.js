// Arithmatic Operators
let a = 10;
let b = 5;

c= a + b; // Addition
console.log("Addition: " + c);

c = a - b; // Subtraction
console.log("Subtraction: " + c);

c = a * b; // Multiplication
console.log("Multiplication: " + c);

c = a / b; // Division
console.log("Division: " + c);

c = a % b; // Modulus
console.log("Modulus: " + c);




// Assignment Operators
let x = 10;

x += 5; // x = x + 5
console.log(x);   //15

x -= 3; // x = x - 3
console.log(x);   //12

x *= 2; // x = x * 2
console.log(x);  //24

x /= 4; // x = x / 4
console.log(x);    //6

x %= 3; // x = x % 3
console.log(x);    //0


// Comparison Operators

let p = 10;
let q = 20;

console.log(p == q); // Equal to
console.log(p != q); // Not equal to
console.log(p > q);  // Greater than
console.log(p < q);  // Less than
console.log(p >= q); // Greater than or equal to
console.log(p <= q); // Less than or equal to   
console.log(p === q); // Strict equal to
console.log(p !== q); // Strict not equal to           


